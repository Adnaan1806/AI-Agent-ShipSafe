export function generateValue(fieldName, originalValue) {
  if (isRealistic(originalValue)) return originalValue;

  const key = normalize(fieldName);

  // Exact match first
  if (PATTERNS.has(key)) return PATTERNS.get(key)();

  // Partial match (longest match wins)
  let best = null;
  let bestLen = 0;
  for (const [pattern] of PATTERNS) {
    if (key.includes(pattern) && pattern.length > bestLen) {
      best = pattern;
      bestLen = pattern.length;
    }
  }
  if (best) return PATTERNS.get(best)();

  // Type-based fallback
  if (typeof originalValue === 'number') return randInt(1, 100);
  if (typeof originalValue === 'boolean') return true;
  if (typeof originalValue === 'string') {
    if (originalValue.includes('@')) return `user${randInt(100, 999)}@example.com`;
    return `test_${fieldName}_${randInt(1, 99)}`;
  }
  return originalValue;
}

export function generateBody(template) {
  if (!template || typeof template !== 'object' || Array.isArray(template)) return template;
  const result = {};
  for (const [k, v] of Object.entries(template)) {
    result[k] = typeof v === 'object' && v !== null && !Array.isArray(v)
      ? generateBody(v)
      : generateValue(k, v);
  }
  return result;
}

// Returns true if the value already looks like realistic data (not a placeholder).
function isRealistic(val) {
  if (val === null || val === undefined) return false;
  if (typeof val !== 'string') return true;
  const lower = val.toLowerCase().trim();
  const placeholders = ['string', 'value', 'test', 'example', 'sample', 'placeholder', 'foo', 'bar', 'baz', 'lorem', '<string>'];
  return !placeholders.some(p => lower === p || lower.startsWith(p + '_'));
}

function normalize(s) { return s.toLowerCase().replace(/[_\-\s]/g, ''); }
function sample(arr) { return arr[Math.floor(Math.random() * arr.length)]; }
function randInt(min, max) { return Math.floor(Math.random() * (max - min + 1)) + min; }
function randHex(n) { return Array.from({ length: n }, () => Math.floor(Math.random() * 16).toString(16)).join(''); }
